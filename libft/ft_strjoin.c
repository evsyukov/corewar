#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	char	*start;
	size_t	len;

	if (!s1 && !s2)
		return (NULL);
	len = ((s1 ? ft_strlen(s1) : 0) + (s2 ? ft_strlen(s2) : 0));
	result = ft_strnew(len);
	if ((start = result))
	{
		if (s1)
			while (*s1)
				*result++ = *s1++;
		if (s2)
			while (*s2)
				*result++ = *s2++;
		*result = '\0';
	}
	return (start);
}
