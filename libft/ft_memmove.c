#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t i;

	i = 0;
	if (len == 0)
		return (dst);
	if (src > dst)
		ft_memcpy(dst, src, len);
	else if (src < dst)
		while (++i <= len)
			((char*)dst)[len - i] = ((char *)src)[len - i];
	return (dst);
}
